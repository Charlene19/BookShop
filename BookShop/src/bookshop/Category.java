package bookshop;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.JoinTable;
import javax.persistence.ManyToMany;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Charl√®ne
 */
@Entity
@Table(name = "CATEGORY")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Category.findAll", query = "SELECT c FROM Category c")
    , @NamedQuery(name = "Category.findByCategoryId", query = "SELECT c FROM Category c WHERE c.categoryId = :categoryId")
    , @NamedQuery(name = "Category.findByCategoryName", query = "SELECT c FROM Category c WHERE c.categoryName = :categoryName")})
public class Category implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @Column(name = "CATEGORY_ID")
    private Integer categoryId;
    @Basic(optional = false)
    @Column(name = "CATEGORY_NAME")
    private String categoryName;
    @JoinTable(name = "ASSOC_BOOK_CATEGORY", joinColumns = {
        @JoinColumn(name = "CATEGORY_ID", referencedColumnName = "CATEGORY_ID")}, inverseJoinColumns = {
        @JoinColumn(name = "BOOK_ISBN", referencedColumnName = "BOOK_ISBN")})
    @ManyToMany
    private Collection<Book> bookCollection;
    @JoinTable(name = "ASSOC_CATEGORY_CATEGORY", joinColumns = {
        @JoinColumn(name = "CATEGORY_CHILD_ID", referencedColumnName = "CATEGORY_ID")}, inverseJoinColumns = {
        @JoinColumn(name = "CATEGORY_PARENT_ID", referencedColumnName = "CATEGORY_ID")})
    @ManyToMany
    private Collection<Category> categoryCollection;
    @ManyToMany(mappedBy = "categoryCollection")
    private Collection<Category> categoryCollection1;

    public Category() {
    }

    public Category(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public Category(Integer categoryId, String categoryName) {
        this.categoryId = categoryId;
        this.categoryName = categoryName;
    }

    public Integer getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    @XmlTransient
    public Collection<Book> getBookCollection() {
        return bookCollection;
    }

    public void setBookCollection(Collection<Book> bookCollection) {
        this.bookCollection = bookCollection;
    }

    @XmlTransient
    public Collection<Category> getCategoryCollection() {
        return categoryCollection;
    }

    public void setCategoryCollection(Collection<Category> categoryCollection) {
        this.categoryCollection = categoryCollection;
    }

    @XmlTransient
    public Collection<Category> getCategoryCollection1() {
        return categoryCollection1;
    }

    public void setCategoryCollection1(Collection<Category> categoryCollection1) {
        this.categoryCollection1 = categoryCollection1;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (categoryId != null ? categoryId.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Category)) {
            return false;
        }
        Category other = (Category) object;
        if ((this.categoryId == null && other.categoryId != null) || (this.categoryId != null && !this.categoryId.equals(other.categoryId))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "bookshop.Category[ categoryId=" + categoryId + " ]";
    }
    
}
